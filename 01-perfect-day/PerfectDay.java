public class PerfectDay{
    public static void main(String[] args){
      System.out.println("Here's my perfect day:");
      morning();
      afternoon();
      evening();
    }//end main


    public static void morning(){
      System.out.print("in the morning, i would eat breakfast.");
      System.out.println("Then play video games.");
    }//end morning
    public static void afternoon(){
      System.out.print("in the afternoon, lunch.");
      System.out.println("Then play video games.");
    }//end afternoon

    public static void evening(){
      System.out.print("in the evening, i would eat dinner.");
      System.out.print("Then play video games.");
      System.out.print("After playing, i sleep.");
    }//end evening




}//end class
